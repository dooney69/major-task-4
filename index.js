function changeBg(color){
    document.body.style.background = color;
    if(color == '#0392BF'){
        document.body.style.color = '#FFFFFF'
    }
    else if (color == '#ED48BA'){
        document.body.style.color = '#FFFFFF'
    }
    else if (color == '#FBEC06'){
        document.body.style.color = '#000000'
    }
    else if (color == '#B3D4E0'){
        document.body.style.color = '#000000'
    }}
